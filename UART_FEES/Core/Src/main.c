void State_Updater(){
	int a = atoi(rx_buffer);

	if(a == 00) led_off();

	if(a == 01) led_on() ;
	if(a == 02) led_off() ;

	if(a == 03) led_on() ;
	if(a == 04) led_off() ;

	if(a == 05) FLAG=1 ;
	if(a == 06) FLAG=0 ;

	if(a == 07) led_on() ;
	if(a == 8) led_off() ;

	if(a == 9) led_on() ;
	if(a == 10) led_off() ;

}

